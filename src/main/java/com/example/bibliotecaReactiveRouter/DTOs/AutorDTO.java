package com.example.bibliotecaReactiveRouter.DTOs;

public class AutorDTO {

    private String idAutor;
    private String nombreAutor;
    private String idNacionalidadAutor;

    public String getIdAutor() {
        return idAutor;
    }

    public void setIdAutor(String idAutor) {
        this.idAutor = idAutor;
    }

    public String getNombreAutor() {
        return nombreAutor;
    }

    public void setNombreAutor(String nombreAutor) {
        this.nombreAutor = nombreAutor;
    }

    public String getIdNacionalidadAutor() {
        return idNacionalidadAutor;
    }

    public void setIdNacionalidadAutor(String idNacionalidadAutor) {
        this.idNacionalidadAutor = idNacionalidadAutor;
    }
}
